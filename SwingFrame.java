    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        // TODO add your handling code here:
            try {
                playertes.SetPath(ArraySong[IndexSong]);
            } catch (FileNotFoundException ex) {
                Logger.getLogger(SwingFrame.class.getName()).log(Level.SEVERE, null, ex);
            } catch (JavaLayerException ex) {
                Logger.getLogger(SwingFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
            tes = new Thread(playertes);
            System.out.println(playertes.GetPausedFrame());
            tes.start();
            isPaused = false;
            if (isPaused) {
                System.out.println("I am alive");

                playertes.GetPlayer().setPlayBackListener(new PlaybackListener() {
                    public void playbackFinished(PlaybackEvent event) {
                        playertes.SetPausedOnFrame(event.getFrame() / 10);
                    }
                });
            } else {
                System.out.println("I am dead");

                playertes.GetPlayer().setPlayBackListener(new PlaybackListener() {
                    public void playbackFinished(PlaybackEvent event) {
                        playertes.SetPausedOnFrame(playertes.GetPausedFrame() + event.getFrame() / 10);
                    }
                });
            }
    }//GEN-LAST:event_jButton1ActionPerformed

